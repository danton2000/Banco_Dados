-- MOSTRE O NOME DOS CLIENTES QUE JÁ FIZERAM PEDIDO

-- JUNÇÃO
SELECT
CLIE.NOME
FROM CLIENTE CLIE
INNER JOIN PEDIDO PEDI ON PEDI.COD_CLI = CLIE.COD_CLI
ORDER BY CLIE.NOME;

-- IN
SELECT
CLIE.NOME
FROM CLIENTE CLIE
WHERE CLIE.COD_CLI IN 
(
	SELECT
	PEDI.COD_CLI
	FROM
	PEDIDO PEDI
)
ORDER BY CLIE.NOME;

-- EXISTS
SELECT
CLIE.NOME
FROM CLIENTE CLIE
WHERE EXISTS 
(
	SELECT
	PEDI.COD_CLI
	FROM PEDIDO PEDI
	WHERE PEDI.COD_CLI = CLIE.COD_CLI
)
ORDER BY CLIE.NOME;